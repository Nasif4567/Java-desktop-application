    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(Usernametxt.getText().equals("")||Passwordtxt.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter username and password");
        }
        
        else {
            
        
        
        String sql = "SELECT * from Admin WHERE Username LIKE ? AND Password LIKE ?;" ;
        try {
           pst = con.prepareStatement(sql); 
           pst.setString(1, Usernametxt.getText());
           pst.setString(2, Passwordtxt.getText());
           rs = pst.executeQuery();
           
           
           if(rs.next()) {
            JOptionPane.showMessageDialog(null, "Login successful");
            rs.close();
            pst.close();
            Dashboard d = new Dashboard();
            d.show();
            this.hide();
            Usernametxt.setText("");
            Passwordtxt.setText("");
            
        }
           
           else {
                 JOptionPane.showMessageDialog(null,"Invalid username and password");

           }
           
          
        }
        
        
        
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Login Error"+e);
        }
        
        
        }//--------------end of else 
    }//GEN-LAST:event_jButton1ActionPerformed

